package com.callippus.water.erp.web.rest.dto;

/**
 * A DTO representing the XML.
 */
public class XmlDTO {

    private String xml;

	public String getXml() {
		return xml;
	}

	public void setXml(String xml) {
		this.xml = xml;
	}

	@Override
	public String toString() {
		return "XmlDTO [xml=" + xml + "]";
	}

}
